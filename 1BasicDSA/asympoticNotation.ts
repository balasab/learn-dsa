class AsympoticNotation {
    BigO() {
        /*
        Big O notation is used to describe the upper bound of the time complexity of an algorithm
        It is used to describe the upper bound of the space complexity of an algorithm
        Used in Worst case 
        */
    }
    BigTheta() {
        /*
        Big Theta notation is used to describe the tight bound of the time complexity of an algorithm
        It is used to describe the tight bound of the space complexity of an algorithm
        Used in Average case
        */
    }
    BigOmega() {
        /*
        Big Omega notation is used to describe the lower bound of the time complexity of an algorithm
        It is used to describe the lower bound of the space complexity of an algorithm
        Used in Best case
        */
    }
}